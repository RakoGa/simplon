let n1 = parseInt(prompt("Saisissez un premier nombre"));
let n2 = parseInt(prompt("Saisissez un second nombre"));

document.write(n1 + " + " + n2 + " = " + (n1+n2));
document.write("<br>");
document.write(n1 + " - " + n2 + " = " + (n1-n2));
document.write("<br>");
document.write(n1 + " * " + n2 + " = " + (n1*n2));
document.write("<br>");
document.write(n1 + " / " + n2 + " = " + (n1/n2));
document.write("<br>");
document.write(n1 + " % " + n2 + " = " + (n1%n2));